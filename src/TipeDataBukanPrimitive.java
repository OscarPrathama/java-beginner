public class TipeDataBukanPrimitive {
    public static void main(String[] args) {
        /**
         * tipe data primitive adalah tipe data bawa'an dalam bahasa pemrograman, tipe data primitive tidak bisa dirubah lagi
         * tipe data number, char, boolean adalah tipe data primitif, tipe data primitive selalu memiliki default value
         * tipe data string bukan tipe data primitif, tipe data bukan primitif tidak memiliki default value, dan bisa bernilai null
         * tipe data bukan primitif bisa memiliki method/function
         * */

        // tipe data bukan primitif
        Integer iniInteger = 10;
        Long iniLong = 10L;
        Boolean iniBoolean = true;

        Short iniShort; // null
        iniShort = 100;
    }
}
